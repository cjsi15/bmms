$(document).ready(function(){
  $(".borrow").click(function(){
  	 $(".intro").hide();
  	 $(".col-sm-4").show();
  	 $('.table').show();
  	  $(".borrowee").show();
  	  $('.formstock').hide();
  	  $('.form-table').hide();
  });
   $(".addstocks").click(function(){
  	 $(".intro").hide();
  	 $('.formstock').show();
  	 $('.form-table').show();
  	 $('.borrowee').toggle();
  });
});
